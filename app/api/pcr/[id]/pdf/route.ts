/**
 * Launch a Chromium instance suitable for the deployment target.
 * - On Vercel/AWS Lambda: uses @sparticuz/chromium (small layered binary).
 * - Locally: respects PUPPETEER_EXECUTABLE_PATH or falls back to default.
 */
async function launchBrowser() {
  const executablePath =
    process.env.PUPPETEER_EXECUTABLE_PATH || (await chromium.executablePath());
  return puppeteer.launch({
    args: chromium.args,
    executablePath,
    headless: true,
    defaultViewport: chromium.defaultViewport,
  });
}

export async function GET(_req: NextRequest, { params }: { params: { id: string } }) {
  try {
    const sb = getServerSupabase();
    const { data: p, error } = await sb.from('pcrs').select('*').eq('id', params.id).maybeSingle();
    if (error) return NextResponse.json({ error: error.message }, { status: 500 });
    if (!p) return NextResponse.json({ error: 'Not found' }, { status: 404 });

    const html = buildHtml(p);
    const browser = await launchBrowser();
    try {
      const page = await browser.newPage();
      await page.setContent(html, { waitUntil: 'networkidle0' });
      const pdf = await page.pdf({ format: 'Letter', printBackground: true,
        margin: { top: '14mm', right: '12mm', bottom: '14mm', left: '12mm' } });
      return new NextResponse(pdf, {
        status: 200,
        headers: {
          'Content-Type': 'application/pdf',
          'Content-Disposition': `attachment; filename="pcr-${params.id}.pdf"`,
          'Cache-Control': 'no-store',
        },
      });
    } finally {
      await browser.close();
    }
  } catch (e: any) {
    return NextResponse.json({ error: e?.message || 'PDF generation failed' }, { status: 500 });
  }
}
