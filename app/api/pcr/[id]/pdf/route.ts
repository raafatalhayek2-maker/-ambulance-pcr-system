function esc(v: any): string {
  if (v == null) return '';
  return String(v).replace(/[&<>'"]/g, c => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]!));
}

function row(label: string, value: any): string {
  return `<tr><th>${esc(label)}</th><td>${esc(value || 'not documented')}</td></tr>`;
}

